package com.Stream.entity;


import java.util.ArrayList;
import java.util.Date;
import java.util.HashSet;
import java.util.List;


public class LolOrder implements java.io.Serializable {

	// Fields

	private Long id;
	private Shopqq shopqq;
	private Long total;
	private String address;
	private String beizhu;
	private String status;
	private Date ODate;
	private String phone;
	private String cnee;
	private List<LolOrderdetail> lolOrderdetails = new ArrayList<LolOrderdetail>(0);

	// Constructors

	/** default constructor */
	public LolOrder() {
	}

	/** full constructor */
	public LolOrder(Shopqq shopqq, Long total, String address, String beizhu,
			String status, Date ODate, String phone, String cnee,
			List<LolOrderdetail> lolOrderdetails) {
		this.shopqq = shopqq;
		this.total = total;
		this.address = address;
		this.beizhu = beizhu;
		this.status = status;
		this.ODate = ODate;
		this.phone = phone;
		this.cnee = cnee;
		this.lolOrderdetails = lolOrderdetails;
	}

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	public Shopqq getShopqq() {
		return this.shopqq;
	}

	public void setShopqq(Shopqq shopqq) {
		this.shopqq = shopqq;
	}

	public Long getTotal() {
		return this.total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public String getAddress() {
		return this.address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getBeizhu() {
		return this.beizhu;
	}

	public void setBeizhu(String beizhu) {
		this.beizhu = beizhu;
	}

	public String getStatus() {
		return this.status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Date getODate() {
		return this.ODate;
	}

	public void setODate(Date ODate) {
		this.ODate = ODate;
	}

	public String getPhone() {
		return this.phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCnee() {
		return this.cnee;
	}

	public void setCnee(String cnee) {
		this.cnee = cnee;
	}

	public List<LolOrderdetail> getLolOrderdetails() {
		return this.lolOrderdetails;
	}

	public void setLolOrderdetails(List<LolOrderdetail> lolOrderdetails) {
		this.lolOrderdetails = lolOrderdetails;
	}

}