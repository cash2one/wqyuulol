package com.Stream.entity;

import java.util.HashSet;
import java.util.Set;

public class Peoinfo implements java.io.Serializable {

	// Fields

	private Long peopleinfoId;
	private Long PVictory;
	private Long PVicLv;
	private Set<People> peoples = new HashSet<People>(0);

	// Constructors

	/** default constructor */
	public Peoinfo() {
	}

	/** full constructor */
	public Peoinfo(Long PVictory, Long PVicLv, Set<People> peoples) {
		this.PVictory = PVictory;
		this.PVicLv = PVicLv;
		this.peoples = peoples;
	}

	public Long getPeopleinfoId() {
		return this.peopleinfoId;
	}

	public void setPeopleinfoId(Long peopleinfoId) {
		this.peopleinfoId = peopleinfoId;
	}

	public Long getPVictory() {
		return this.PVictory;
	}

	public void setPVictory(Long PVictory) {
		this.PVictory = PVictory;
	}

	public Long getPVicLv() {
		return this.PVicLv;
	}

	public void setPVicLv(Long PVicLv) {
		this.PVicLv = PVicLv;
	}

	public Set<People> getPeoples() {
		return this.peoples;
	}

	public void setPeoples(Set<People> peoples) {
		this.peoples = peoples;
	}

}