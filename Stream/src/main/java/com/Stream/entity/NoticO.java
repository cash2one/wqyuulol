package com.Stream.entity;


public class NoticO implements java.io.Serializable {

	// Fields

	private Long id;
	private LolNotic lolNotic;
	private String names;
	private String cont;
	private String img;

	// Constructors

	/** default constructor */
	public NoticO() {
	}

	/** full constructor */
	public NoticO(LolNotic lolNotic, String names, String cont, String img) {
		this.lolNotic = lolNotic;
		this.names = names;
		this.cont = cont;
		this.img = img;
	}

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	public LolNotic getLolNotic() {
		return this.lolNotic;
	}

	public void setLolNotic(LolNotic lolNotic) {
		this.lolNotic = lolNotic;
	}

	public String getNames() {
		return this.names;
	}

	public void setNames(String names) {
		this.names = names;
	}

	public String getCont() {
		return this.cont;
	}

	public void setCont(String cont) {
		this.cont = cont;
	}

	public String getImg() {
		return this.img;
	}

	public void setImg(String img) {
		this.img = img;
	}

}