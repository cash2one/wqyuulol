package com.Stream.entity;


public class LolAddress implements java.io.Serializable {

	// Fields

	private Long id;
	private LolStreet lolStreet;
	private Shopqq shopqq;
	private String detail;
	private String phone;
	private String tel;
	private String mail;
	private String status;

	// Constructors

	/** default constructor */
	public LolAddress() {
	}

	/** full constructor */
	public LolAddress(LolStreet lolStreet, Shopqq shopqq, String detail,
			String phone, String tel, String mail, String status) {
		this.lolStreet = lolStreet;
		this.shopqq = shopqq;
		this.detail = detail;
		this.phone = phone;
		this.tel = tel;
		this.mail = mail;
		this.status = status;
	}

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public LolStreet getLolStreet() {
		return this.lolStreet;
	}

	public void setLolStreet(LolStreet lolStreet) {
		this.lolStreet = lolStreet;
	}

	public Shopqq getShopqq() {
		return this.shopqq;
	}

	public void setShopqq(Shopqq shopqq) {
		this.shopqq = shopqq;
	}

	public String getDetail() {
		return this.detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getPhone() {
		return this.phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getTel() {
		return this.tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getMail() {
		return this.mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getStatus() {
		return this.status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}