package com.Stream.entity;

public class NoticOo implements java.io.Serializable {

	// Fields

	private Long id;
	private LolNotic lolNotic;
	private String names;
	private String status;
	private String cont;
	private String img;
	private String comen;

	// Constructors

	/** default constructor */
	public NoticOo() {
	}

	/** full constructor */
	public NoticOo(LolNotic lolNotic, String names, String status, String cont,
			String img, String comen) {
		this.lolNotic = lolNotic;
		this.names = names;
		this.status = status;
		this.cont = cont;
		this.img = img;
		this.comen = comen;
	}

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	public LolNotic getLolNotic() {
		return this.lolNotic;
	}

	public void setLolNotic(LolNotic lolNotic) {
		this.lolNotic = lolNotic;
	}

	public String getNames() {
		return this.names;
	}

	public void setNames(String names) {
		this.names = names;
	}

	public String getStatus() {
		return this.status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCont() {
		return this.cont;
	}

	public void setCont(String cont) {
		this.cont = cont;
	}

	public String getImg() {
		return this.img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getComen() {
		return this.comen;
	}

	public void setComen(String comen) {
		this.comen = comen;
	}

}